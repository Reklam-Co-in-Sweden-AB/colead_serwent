export function Sidebar({ userRole = "bruker" }: { userRole?: UserRole }) {
  const pathname = usePathname()

  const isActive = (href: string) => {
    if (href === "/admin") return pathname === "/admin"
    return pathname.startsWith(href)
  }

  const visibleItems = NAV_ITEMS.filter((item) => {
    if (!item.minRole) return true
    return ROLE_HIERARCHY.indexOf(userRole) >= ROLE_HIERARCHY.indexOf(item.minRole)
  })

  return (
    <aside className="w-60 bg-dark min-h-[calc(100vh-64px)] flex flex-col shrink-0">
      <nav className="flex flex-col gap-1 p-3 flex-1">
        {visibleItems.map((item) => (
          <Link
            key={item.href}
            href={item.href}
            className={`
              flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all no-underline
              ${isActive(item.href)
                ? "bg-teal/15 text-teal"
                : "text-white/60 hover:bg-white/5 hover:text-white/90"
              }
            `}
          >
            {item.icon}
            {item.label}
          </Link>
        ))}
      </nav>

      <div className="p-3 border-t border-white/10">
        <Link
          href="/"
          className="flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm text-white/40 hover:text-white/70 transition-colors no-underline"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.8}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14" />
          </svg>
          Se bestillingsskjema
        </Link>
      </div>
    </aside>
  )
}
